def get_hard_classes_from_val(
    val_labels,
    val_preds,
    classes,
    acc_threshold=0.82,
    max_classes=10,
    priority_names=None
):
    class_acc_df = make_class_accuracy_table(
        labels=val_labels,
        preds=val_preds,
        classes=classes
    )

    hard_df = class_acc_df[
        class_acc_df["accuracy"] < acc_threshold
    ].copy()

    hard_df = hard_df.sort_values("accuracy", ascending=True)

    hard_class_names = hard_df["class_name"].head(max_classes).tolist()

    # 전자기기 계열은 우선 포함
    if priority_names is not None:
        for name in priority_names:
            if name in classes and name not in hard_class_names:
                hard_class_names.append(name)

    return hard_class_names, class_acc_df


from torch.utils.data import WeightedRandomSampler

def make_hard_class_sampler(
    dataset,
    hard_class_names,
    class_to_idx,
    hard_sample_weight=4.0,
    normal_sample_weight=1.0
):
    hard_label_ids = set([
        class_to_idx[name]
        for name in hard_class_names
        if name in class_to_idx
    ])

    sample_weights = []

    for _, row in dataset.df.iterrows():
        label = int(row["label"])

        if label in hard_label_ids:
            sample_weights.append(hard_sample_weight)
        else:
            sample_weights.append(normal_sample_weight)

    sampler = WeightedRandomSampler(
        weights=sample_weights,
        num_samples=len(sample_weights),
        replacement=True
    )

    return sampler


def make_hard_class_weight_tensor(
    num_classes,
    classes,
    hard_class_names,
    device,
    hard_loss_weight=1.4,
    normal_loss_weight=1.0
):
    weights = torch.ones(num_classes) * normal_loss_weight

    for name in hard_class_names:
        if name in classes:
            idx = classes.index(name)
            weights[idx] = hard_loss_weight

    weights = weights.to(device)

    return weights



