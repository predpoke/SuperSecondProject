def topk_accuracy(outputs, labels, k=3):
    with torch.no_grad():
        _, topk_preds = outputs.topk(k, dim=1)
        correct = topk_preds.eq(labels.view(-1, 1).expand_as(topk_preds))
        return correct.any(dim=1).float().mean().item()

def evaluate(model, val_loader, criterion, device):
    model.eval()

    running_loss = 0.0
    correct = 0
    total = 0

    top3_sum = 0.0
    batch_count = 0

    all_preds = []
    all_labels = []

    start_time = time.time()

    progress_bar = tqdm(val_loader, desc="Validation", leave=True)

    with torch.no_grad():
        for batch_idx, (images, labels) in enumerate(progress_bar):
            batch_start = time.time()

            images = images.to(device)
            labels = labels.to(device)

            outputs = model(images)
            loss = criterion(outputs, labels)

            running_loss += loss.item() * images.size(0)

            _, preds = torch.max(outputs, 1)
            correct += (preds == labels).sum().item()
            total += labels.size(0)

            top3 = topk_accuracy(outputs, labels, k=3)
            top3_sum += top3
            batch_count += 1

            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())

            current_loss = running_loss / total
            current_acc = correct / total
            current_top3 = top3_sum / batch_count
            batch_time = time.time() - batch_start

            progress_bar.set_postfix({
                "batch": batch_idx + 1,
                "loss": f"{current_loss:.4f}",
                "acc": f"{current_acc:.4f}",
                "top3" : f"{current_top3:.4f}",
                "sec/batch": f"{batch_time:.2f}"
            })

    epoch_time = time.time() - start_time

    epoch_loss = running_loss / total
    epoch_acc = correct / total
    epoch_top3 = top3_sum / batch_count

    return epoch_loss, epoch_acc, epoch_top3, all_preds, all_labels, epoch_time

def make_class_accuracy_table(labels, preds, classes) :
  labels = np.array(labels)
  preds = np.array(preds)

  rows = []

  for idx, class_name in enumerate(classes):
    mask = labels == idx
    total = mask.sum()

    if total == 0:
      correct = 0
      acc = 0.0

    else :
      correct = int((preds[mask] == labels[mask]).sum())
      acc = correct/ total

    rows.append({
        "class_idx" : idx,
        "class_name" : class_name,
        "total" : int(total),
        "correct" : int(correct),
        "accuracy" : acc
    })
  return pd.DataFrame(rows).sort_values("accuracy", ascending=True)

