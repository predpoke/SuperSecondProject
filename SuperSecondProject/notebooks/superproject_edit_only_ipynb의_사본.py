class CenterLoss(nn.Module):
    def __init__(self, num_classes, feat_dim, device):
        super().__init__()

        self.num_classes = num_classes
        self.feat_dim = feat_dim

        self.centers = nn.Parameter(
            torch.randn(num_classes, feat_dim).to(device)
        )

    def forward(self, features, labels):
        """
        features: [batch, feat_dim]
        labels  : [batch]
        """
        batch_centers = self.centers[labels]

        loss = ((features - batch_centers) ** 2).sum(dim=1).mean() / 2.0

        return loss


def convnext_forward_with_features(model, images):
    """
    ConvNeXt에서 classifier 직전 feature와 logits를 함께 반환.
    Dropout을 classifier에 추가한 경우도 대응.
    """

    x = model.features(images)
    x = model.avgpool(x)

    # classifier[0] = LayerNorm2d
    # classifier[1] = Flatten
    # classifier[2] = Linear 또는 Sequential(Dropout, Linear)
    x = model.classifier[0](x)
    features = model.classifier[1](x)

    logits = model.classifier[2](features)

    return logits, features


def get_convnext_feature_dim(model):
    head = model.classifier[2]

    if isinstance(head, nn.Linear):
        return head.in_features

    if isinstance(head, nn.Sequential):
        for layer in head:
            if isinstance(layer, nn.Linear):
                return layer.in_features

    raise TypeError(f"Linear head를 찾을 수 없습니다: {type(head)}")



def train_one_epoch_centerloss_dualview(
    model,
    train_loader,
    criterion,
    center_loss_fn,
    optimizer,
    center_optimizer,
    device,
    center_loss_weight=0.02
):
    model.train()
    center_loss_fn.train()

    running_loss = 0.0
    running_ce_loss = 0.0
    running_center_loss = 0.0

    correct = 0
    total = 0

    start_time = time.time()

    progress_bar = tqdm(
        train_loader,
        desc=f"Training CenterLoss Phase weight={center_loss_weight}",
        leave=True
    )

    for batch_idx, (images_global, images_local, labels) in enumerate(progress_bar):
        batch_start = time.time()

        # Phase 2에서는 global view만 사용
        images = images_global.to(device, non_blocking=True)
        labels = labels.to(device, non_blocking=True)

        optimizer.zero_grad(set_to_none=True)
        center_optimizer.zero_grad(set_to_none=True)

        with autocast():
            logits, features = convnext_forward_with_features(
                model,
                images
            )

            ce_loss = criterion(logits, labels)

            c_loss = center_loss_fn(
                features,
                labels
            )

            loss = ce_loss + center_loss_weight * c_loss

        scaler.scale(loss).backward()

        scaler.step(optimizer)
        scaler.step(center_optimizer)
        scaler.update()

        running_loss += loss.item() * labels.size(0)
        running_ce_loss += ce_loss.item() * labels.size(0)
        running_center_loss += c_loss.item() * labels.size(0)

        _, preds = torch.max(logits, 1)

        correct += (preds == labels).sum().item()
        total += labels.size(0)

        current_loss = running_loss / total
        current_ce = running_ce_loss / total
        current_center = running_center_loss / total
        current_acc = correct / total
        batch_time = time.time() - batch_start

        progress_bar.set_postfix({
            "loss": f"{current_loss:.4f}",
            "ce": f"{current_ce:.4f}",
            "center": f"{current_center:.4f}",
            "acc": f"{current_acc:.4f}",
            "sec/batch": f"{batch_time:.2f}"
        })

    epoch_time = time.time() - start_time

    epoch_loss = running_loss / total
    epoch_acc = correct / total
    epoch_ce_loss = running_ce_loss / total
    epoch_center_loss = running_center_loss / total

    return (
        epoch_loss,
        epoch_acc,
        epoch_ce_loss,
        epoch_center_loss,
        epoch_time
    )

