class OfficeHomeDualViewDataset(Dataset):
    def __init__(self, csv_path, transform_global=None, transform_local=None):
        self.df = pd.read_csv(csv_path)
        self.transform_global = transform_global
        self.transform_local = transform_local

    def __len__(self): #Dataloader가 전체 데이터 개수를 알아야 batch 를 만들 수 있어서 꼭 필요함
        return len(self.df)

    def __getitem__(self, idx): # idx 번째 데이터를 꺼내는 함수
        row = self.df.iloc[idx]

        image_path = row["image_path"]
        label = int(row["label"])

        image = Image.open(image_path).convert("RGB")


        if self.transform_global :
          image_global = self.transform_global(image)
        else :
          image_global = image

        if self.transform_local :
          image_local = self.transform_local(image)
        else :
          image_local = image



        return image_global, image_local, label

class OfficeHomeCSVDataset(Dataset):
    def __init__(self, csv_path, transform=None, return_path=False):
        self.df = pd.read_csv(csv_path)
        self.transform = transform
        self.return_path = return_path

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]

        image_path = row["image_path"]
        label = int(row["label"])

        image = Image.open(image_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        if self.return_path:
            return image, label, image_path

        return image, label

