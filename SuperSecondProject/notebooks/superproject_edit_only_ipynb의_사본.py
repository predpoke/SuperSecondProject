def evaluate_clean_train_acc(
    model,
    train_loader,
    criterion,
    device,
    max_batches=None
):
    model.eval()

    running_loss = 0.0
    correct = 0
    total = 0

    start_time = time.time()

    with torch.no_grad():
        for batch_idx, (images, labels) in enumerate(tqdm(train_loader, desc="Clean Train Eval", leave=True)):
            if max_batches is not None and batch_idx >= max_batches:
                break

            images = images.to(device)
            labels = labels.to(device)

            with autocast():
                outputs = model(images)
                loss = criterion(outputs, labels)

            running_loss += loss.item() * images.size(0)

            _, preds = torch.max(outputs, 1)

            correct += (preds == labels).sum().item()
            total += labels.size(0)

    epoch_time = time.time() - start_time

    clean_loss = running_loss / total
    clean_acc = correct / total

    return clean_loss, clean_acc, epoch_time



def add_dropout_to_convnext_classifier(model, dropout_p=0.1):
    old_head = model.classifier[2]

    if isinstance(old_head, nn.Sequential):
        print("classifier가 이미 Sequential입니다. Dropout 추가 생략")
        return model

    if not isinstance(old_head, nn.Linear):
        raise TypeError(f"예상한 Linear가 아닙니다: {type(old_head)}")

    in_features = old_head.in_features
    out_features = old_head.out_features

    new_head = nn.Sequential(
        nn.Dropout(p=dropout_p),
        nn.Linear(in_features, out_features)
    )

    with torch.no_grad():
        new_head[1].weight.copy_(old_head.weight)
        new_head[1].bias.copy_(old_head.bias)

    model.classifier[2] = new_head

    print(f"Dropout 추가 완료: p={dropout_p}")

    return model

